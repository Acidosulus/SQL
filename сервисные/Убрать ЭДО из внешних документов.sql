begin
  update stack.[Внешние документы]
  set [Договор-Файл] = -1
	  where
			  [Договор-Файл]=116602
		  and	[Документ-Файл]>0
		  and (
					  ([Оригинальное имя] like 'ON_PRSCHET_%' and [Комментарий] like 'Счет на предоплату%' and [Комментарий] like '%(ЭДО)%')
