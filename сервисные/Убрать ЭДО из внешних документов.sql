				  or 	([Оригинальное имя] like 'ON_NSCHFDOPPR%' and [Комментарий] like 'Счет-фактура №%' and [Комментарий] like '%(ЭДО)%')
				  or 	([Оригинальное имя] like 'ON_APP_%' and [Комментарий] like 'Акт приема-передачи электроэнергии №%' and [Комментарий] like '%(ЭДО)%')
				  or 	([Оригинальное имя] like 'ON_VEDEHEH_%' and [Комментарий] like 'Ведомость электропотребления №%' and [Комментарий] like '%(ЭДО)%')
